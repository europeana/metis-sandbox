package eu.europeana.metis.sandbox.entity;

/**
 * Represents the type of an XSLT file.
 */
public enum XsltType {
  /**
   * Represents the default XSLT file for transformation tasks.
   */
  DEFAULT,

  /**
   * Represents the XSLT for transformation to EDM-EXTERNAL from another unknown to europeana model.
   */
  EXTERNAL
}
